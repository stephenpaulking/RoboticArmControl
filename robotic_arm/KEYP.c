/* Variable to hold keypad value */

int KeyCode; 
 
/*--------------------------------------------------------------------------*
 * Operation Code :-                                                        *
 *--------------------------------------------------------------------------*/
  
int KEYP__GetState( void )
{
    KeyCode = IORD_ALTERA_AVALON_PIO_DATA(KEYPAD_0_BASE);   /* Read keypad base address */
    
    return KeyCode;                                         /* Return keypad value */
}

